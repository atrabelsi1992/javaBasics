package com.syntax.class06;

public class SwitchLimiations {

	public static void main(String[] args) {
		
		/*
		 * Switch cases work with: String, byte, short, int, char.
		 * Switch cases do NOT work with: boolean (only two options), 
		 *   double (to many variables), float
		 *   boolean happy=true;
		 *   switch (happy) {}
		 *   long number=10001;
		 *   switch (number) {}
		 
		 *   double number=1.99;
		 *   switch (number) {}
		 *   
		 *   Switch case can't be used with Relational or Logical Operators 
		 *   
		 *   int num1=10;
		 *   switch(num1) {
		 *   case num1>=10:
		 *   case num1>=10 && num1<=100
		 *   }
		 *   
		 */
	}
}